#include "push_swap.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while ((s1[i] || s2[i]) && s1[i] == s2[i])
		i++;
	if ((!s1[i] || s1[i] == '\n') && (!s2[i] || s2[i] == '\n'))
		return (0);
	else
		return (s1[i] - s2[i]);
}
